        <h1>
            About Mineshafter Squared
        </h1>
        <div class="info_track">
            <p>
                Mineshafter Squared is an authentication and texture proxy for the
                popular game <a href-"http://minecraft.net">Minecraft</a>
                created by <a href="http://notch.tumblr.com/">Notch</a> of
                <a href="http://mojang.com/">Mojang</a>. It gets in the
                middle of the game clients and its outbound calls to the minecraft
                servers. This allows my service to provide its own login system for
                the game which enables players to try out the game before buying. My
                authentication service is also online whenever the official
                Minecraft server's go down. That is not as much a problem anymore,
                but was very useful in the past.
            </p>
            <p>
                The proxy also allows me to offer an amazing texture management
                system that far outshines the official service. Users can upload
                multiple textures to switch between, discover new textures, and
                share them with their friends and the community.
            </p>
        </div>
